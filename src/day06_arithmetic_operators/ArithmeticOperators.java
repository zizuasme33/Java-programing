package day06_arithmetic_operators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        System.out.println(5 + 10);
        System.out.println(10 - 3);
        System.out.println(3 * 3);
        System.out.println(15/5);
        System.out.println(13 % 5); // 5 + 5 + (3 is remainder)

        System.out.println(-2 * 2); // -4
        int chairs = 4 + 2;
        System.out.println(chairs); // 6 will be the result


        int spoons = 10 + 20 + 12 + 0;
        System.out.println(spoons);

        // There are 42 spoon in kitchen
        System.out.println("There are " + spoons + " spoons in kitchen.");



    }
}
